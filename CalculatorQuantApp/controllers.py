class CalculatorQuant:
    def quant_add(self, a, b):
        return a + b

    def quant_subtract(self, a, b):
        return a - b

    def quant_multiply(self, a, b):
        return a * b

    def quant_divide(self, a, b):
        return a/b

    def quant_divide_abs(self, a, b):
        return a//b


    def quant_add_abs(self, a, b):
        return abs(a+b)